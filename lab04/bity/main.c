#include <stdio.h>
#include <stdlib.h>

int main()
{
    int a =11, b=6;
    printf("a&b= %d\n", a&b);
    printf("a|b= %d\n", a|b);
    printf("a^b= %d\n", a^b);
    printf("a<<1= %d\n", a<<1);
    printf("a<<2= %d\n", a<<2);
    printf("a>>1= %d\n", a>>1);
    printf("a>>2= %d\n", a>>2);
    printf("~a= %d\n", ~a);
    return 0;
}
